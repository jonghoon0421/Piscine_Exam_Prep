#include <unistd.h>

void	ft_rev(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	i = i  - 1;
	while (i >= 0)
	{
		write(1, &str[i], 1);
		i--;
	}
}

int		main(int ac, char **av)
{
	if (ac != 2)
		write(1, "\n", 1);
	else
		ft_rev(av[1]);
	return (0);
}
