int		ft_atoi(const char *str)
{
	int i;
	int negative;
	int n;

	i = 0;
	negative = 0;
	n = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
			|| str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-')
		negative = 1;
	if (str[i] == '-'|| str[i] == '+')
		i++;
	while (str[i] != '\0' && str[i] <= '9' && str[i] >= '0')
		{
			n = n * 10;
			n = n + str[i] - 48;
			i++;
		}
	if (negative == 1)
		n = (-1) * n;
	return (n);
}
