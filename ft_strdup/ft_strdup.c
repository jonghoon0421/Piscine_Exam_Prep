char	*ft_strdup(char *src)
{
	char *dest;
	int i;
	int j;

	i = 0;
	j = 0;
	while (src[i] != '\0')
		i++;
	dest = (char*)malloc(sizeof(char) * (i + 1));

	while (j < i)
	{
		dest[j] = src[j];
		j++;
	}
	dest[j] = '\0';
	return (dest);
}
